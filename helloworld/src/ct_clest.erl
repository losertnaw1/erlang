-module(ct_clest).
-behaviour(gen_server).
-export([init/1, handle_call/3, handle_cast/2, handle_info/2, terminate/2, code_change/3]).

%% ====================================================================
%% API functions
%% ====================================================================
-export([s/1,send/4]).

s(NodeName) ->
	{error,{already_started,Pid_sv}} = ct_svest:s(),
	{ok, Pid} = gen_server:start_link({local,?MODULE}, ?MODULE, [NodeName], []),
	io:format("Client ~p registed : ~p ~n", [NodeName,Pid]),
	ct_svest:rs(NodeName,Pid_sv,Pid).

send(NodeName,Pid_sv,To,Msg) ->
	ct_svest:send(NodeName,Pid_sv,To,Msg).
